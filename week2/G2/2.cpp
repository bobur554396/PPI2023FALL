#include <iostream>

using namespace std;

int main(){
    /*

        increment / decrement the values of current variable
    */

    int a = 2; // 2
    a = a + 3; // 5
    a = a + 1; // 6
    a = a - 3; // 3
    a = a * 4; // 12
    a = a / 2; // 6
    a = a % 4; // 2

    int b = 2; // 2
    b += 3; // b = b + 3;
    b += 1; // b = b + 1;
    b -= 3;
    b *= 4;
    b /= 2;
    b %= 4;

    int c = 2;
    c++; // c++ => c = c + 1; => 3
    c--; // c-- => c = c - 1; => 2




    return 0;
}