#include <iostream>

using namespace std;

int main(){
    // - [x] comparison operators (==, !=, >, <, >=, <=)

    bool isEqual = (2 == 2);

    cout << isEqual << endl; // is number 2 equals to number 2?
    cout << (1 != 2) << endl; // is number 1 not equal to number 2?
    cout << (1 < 2) << endl; // is number 1 is less than number 2?



    return 0;
}